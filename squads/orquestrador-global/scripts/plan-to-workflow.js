#!/usr/bin/env node
/**
 * plan-to-workflow.js — STORY-LC-7 (/plan-run --live).
 *
 * Translates a VALIDATED orchestrator plan into a ready-to-run Workflow script that drives the plan LIVE:
 * topological node dispatch (single → /swarm-execute, loop → /loop-compiler→Workflow), output threading, budget
 * ceiling, and HALT-and-resume gates. Mirrors driveRun's logic, emitted as a standalone Workflow string.
 *
 * Compile ≠ execute (ADR-LC-005): this EMITS the Workflow; it never runs it. The operator runs the emitted script
 * via the Workflow tool. A Workflow cannot prompt mid-run, so each gate (first-loop-review / budget-threshold /
 * human-gate / @devops authority) HALTS the run and returns `paused`; the operator approves out-of-band and resumes
 * by setting APPROVED[node]=true (a review-ack bound to the plan_hash) + FROM_NODE=node.
 *
 * Usage:  node plan-to-workflow.js <plan.json> [--pause-budget N] [--budget-ceiling N]
 *         node plan-to-workflow.js --self-test
 */
'use strict';
const { computeLayers, nodeBudget, depsOf, planHash, nodePauses, PAUSE_BUDGET_DEFAULT } = require('./plan-run');

function nodePrompt(node) {
  const cap = node.capability || node.capability_ref || node.id;
  const label = node.label || cap;
  if (node.execution_kind === 'loop') {
    const hint = node.loop_hint || {};
    // Workflow nesting is ONE level only — a loop node inside this emitted Workflow must NOT spawn a child Workflow.
    // The subagent runs the loop iteratively in its own context; /loop-compiler is optional structure, not a nested run.
    return `[LOOP NODE] Run this as a bounded iterative loop for: ${label} `
      + `(terminate ${hint.termination_hint || 'at its Definition of Done'}${hint.budget_tokens ? `, budget ~${hint.budget_tokens} tokens` : ''}). `
      + `Optionally compile a loop-spec via /loop-compiler ("${hint.task || label}") for structure, but execute the iterations `
      + `IN THIS CONTEXT — do NOT spawn a nested Workflow (Workflow nesting is one level only). Honor maker≠checker + the loop's DoD. Return the loop result.`;
  }
  return `[SINGLE NODE] Dispatch ${cap} via /swarm-execute (or run the agent ${cap}) for: ${label}. Return the artifact/result.`;
}

/** Deterministically resolve order/deps/gates/budgets/prompts, then bake them into a Workflow script string. */
function generate(plan, opts = {}) {
  const dag = plan.dag || {};
  const layers = computeLayers(dag);
  const order = layers.flat();
  const nodeMap = new Map((dag.nodes || []).map(n => [n.id, n]));
  const pauseBudget = opts.pause_budget || PAUSE_BUDGET_DEFAULT;
  const hash = planHash(plan);

  const DEPS = {}, LOOP = {}, GATE = {}, BUDGET = {}, PROMPT = {};
  let firstLoopSeen = false;
  for (const id of order) {
    const node = nodeMap.get(id) || { id };
    const budget = nodeBudget(node);
    const pauses = nodePauses(node, budget, pauseBudget, firstLoopSeen);
    if (node.execution_kind === 'loop') firstLoopSeen = true;
    DEPS[id] = depsOf(node, plan);
    LOOP[id] = node.execution_kind === 'loop';
    GATE[id] = pauses.length ? pauses.join(' + ') : null;
    BUDGET[id] = budget;
    PROMPT[id] = nodePrompt(node);
  }

  const ceiling = opts.budget_ceiling || null;
  const planId = plan.plan_id || plan.slug || 'plan';
  const J = (v) => JSON.stringify(v);

  return `// AUTO-GENERATED by plan-to-workflow.js from plan (${planId}) — DO NOT hand-edit.
// Live drive of a validated plan. Compile ≠ execute (ADR-LC-005): the translator emitted this; it did NOT run it.
// A Workflow cannot prompt mid-run, so each GATE HALTs the run and returns { paused }. Approve out-of-band, then
// re-run with APPROVED["<node>"]=true (a review-ack bound to PLAN_HASH) and FROM_NODE="<node>".
export const meta = {
  name: ${J(planId)},
  description: ${J(`Live drive of plan ${planId} (${order.length} nodes) — HALTs at each gate for operator approval.`)},
  phases: [
${order.map(id => `    { title: ${J(id)}, detail: ${J((LOOP[id] ? 'loop' : 'single') + (GATE[id] ? ` · GATE: ${GATE[id]}` : ''))} }`).join(',\n')}
  ],
}

// Baked plan data (the translator resolved these deterministically — do not edit):
const ORDER = ${J(order)};
const DEPS = ${J(DEPS)};
const LOOP = ${J(LOOP)};
const GATE = ${J(GATE)};
const BUDGET = ${J(BUDGET)};
const PROMPT = ${J(PROMPT)};
const PLAN_HASH = ${J(hash)};

// ── Operator controls (edit before running) ──────────────────────────────────
const APPROVED = {};                 // nodeId -> true to pre-approve a gated node (review-ack for PLAN_HASH)
const FROM_NODE = null;              // resume point: skip nodes before it (must be a node in ORDER)
const PRIOR_OUTPUTS = {};            // on resume: nodeId -> the real output of a skipped node (from the paused run's done)
const BUDGET_CEILING = ${ceiling === null ? 'null' : ceiling};
const RESUMED_SKIP = Symbol('resumed-skip');   // unique sentinel — cannot collide with any real node output

if (FROM_NODE !== null && !ORDER.includes(FROM_NODE)) throw new Error('FROM_NODE "' + FROM_NODE + '" is not a node in the plan');

const done = {};
let cum = 0, resuming = FROM_NODE !== null;
for (const id of ORDER) {
  if (resuming) { if (id === FROM_NODE) resuming = false; else { done[id] = Object.prototype.hasOwnProperty.call(PRIOR_OUTPUTS, id) ? PRIOR_OUTPUTS[id] : RESUMED_SKIP; continue; } }
  if (BUDGET_CEILING && cum + BUDGET[id] > BUDGET_CEILING) {
    log('HALT budget-ceiling at ' + id + ' (cum ' + cum + ' + ' + BUDGET[id] + ' > ' + BUDGET_CEILING + ')');
    return { halted: 'budget-ceiling', at: id, done, total_budget: cum };
  }
  if (GATE[id] && !APPROVED[id]) {
    log('PAUSE at ' + id + ': ' + GATE[id] + '. Review, then re-run with APPROVED["' + id + '"]=true (review-ack for plan ' + PLAN_HASH + ') and FROM_NODE="' + id + '".');
    return { paused: id, gate: GATE[id], plan_hash: PLAN_HASH, done };
  }
  phase(id);
  const inputs = DEPS[id].map(d => ({ from: d, output: done[d] }));
  const missing = inputs.find(x => x.output === RESUMED_SKIP);
  if (missing) throw new Error('resume: node "' + id + '" needs the output of "' + missing.from + '" — set PRIOR_OUTPUTS["' + missing.from + '"] or resume from an earlier node');
  done[id] = await agent(PROMPT[id] + (inputs.length ? '\\n\\nUpstream outputs: ' + JSON.stringify(inputs) : ''), { label: id, phase: id });
  cum += BUDGET[id];
}
return { done, complete: true, total_budget: cum };
`;
}

// ── self-test (deterministic, no agents) ───────────────────────────────────
function selfTest() {
  const plan = {
    plan_id: 'lc7-self-test',
    dag: {
      nodes: [
        { id: 'n1', capability: 'analyst', label: 'discover' },
        { id: 'n2', execution_kind: 'loop', loop_hint: { task: 'sweep until dry', termination_hint: 'until-dry', budget_tokens: 150000 }, depends_on: ['n1'] },
        { id: 'n3', capability: 'devops', label: 'deploy to prod', depends_on: ['n2'] },
      ],
      edges: [{ from: 'n1', to: 'n2' }, { from: 'n2', to: 'n3' }],
    },
  };
  const out = generate(plan);
  const checks = [];
  checks.push(['emits a Workflow with export const meta', /export const meta = \{/.test(out)]);
  checks.push(['ORDER is topological (n1<n2<n3)', /const ORDER = \["n1","n2","n3"\]/.test(out)]);
  checks.push(['loop node n2 references /loop-compiler (optional structure)', out.includes('[LOOP NODE]') && out.includes('/loop-compiler')]);
  checks.push(['loop node does NOT mandate a nested Workflow (one-level limit)', out.includes('do NOT spawn a nested Workflow') && !out.includes('EXECUTE the emitted Workflow')]);
  checks.push(['single node n1 dispatches to /swarm-execute', out.includes('[SINGLE NODE]') && out.includes('/swarm-execute')]);
  checks.push(['n2 (first loop) is gated first-loop-review', /"n2":"[^"]*first-loop-review/.test(out)]);
  checks.push(['n3 (deploy) is gated @devops authority', /"n3":"[^"]*agent-authority/.test(out)]);
  checks.push(['n1 (plain single, cheap) is NOT gated', /"n1":null/.test(out)]);
  checks.push(['threading: n2 depends on n1', /"n2":\["n1"\]/.test(out)]);
  checks.push(['gate HALTs the run (returns paused)', out.includes('return { paused: id')]);
  checks.push(['budget ceiling HALTs', out.includes("halted: 'budget-ceiling'")]);
  checks.push(['resume guard: unknown FROM_NODE throws', out.includes("is not a node in the plan")]);
  checks.push(['plan_hash baked (review-ack binding)', new RegExp('const PLAN_HASH = "' + planHash(plan) + '"').test(out)]);
  checks.push(['compile≠execute banner present', /Compile . execute/.test(out) || out.includes('did NOT run it')]);
  checks.push(['deterministic', generate(plan) === generate(plan)]);

  let pass = 0;
  for (const [name, ok] of checks) { console.log(`  ${ok ? 'PASS' : 'FAIL'}  ${name}`); if (ok) pass++; }
  console.log(`\nself-test: ${pass}/${checks.length} passed`);
  process.exit(pass === checks.length ? 0 : 1);
}

function arg(name) { const i = process.argv.indexOf(name); return i !== -1 ? process.argv[i + 1] : null; }

function main() {
  if (process.argv.includes('--self-test')) return selfTest();
  const planPath = process.argv[2];
  if (!planPath || planPath.startsWith('--')) {
    console.error('Usage: plan-to-workflow.js <plan.json> [--pause-budget N] [--budget-ceiling N] | --self-test');
    process.exit(2);
  }
  const fs = require('fs'); const path = require('path');
  const raw = fs.readFileSync(planPath, 'utf8');
  const plan = /\.ya?ml$/.test(planPath) ? require('js-yaml').load(raw) : JSON.parse(raw);
  const num = (name) => { const v = arg(name); if (v == null) return null; const n = Number(v); if (!Number.isFinite(n) || n <= 0) { console.error(`${name} must be a positive number`); process.exit(2); } return n; };
  const out = generate(plan, { pause_budget: num('--pause-budget') || undefined, budget_ceiling: num('--budget-ceiling') });
  const outPath = path.join(path.dirname(planPath), 'plan.workflow.js');
  fs.writeFileSync(outPath, out);
  console.log(`Wrote ${outPath} (${out.split('\n').length} lines). NOT executed — review then run via the Workflow tool (compile ≠ execute).`);
}

if (require.main === module) main();
module.exports = { generate, nodePrompt };
