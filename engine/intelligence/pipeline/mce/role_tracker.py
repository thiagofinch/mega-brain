def track_roles_and_create(slug: str) -> dict[str, Any]:
    """Scan slug for role mentions, update tracking state, auto-create CRITICAL agents.

    This is the single entry point called from cmd_finalize as a non-blocking
    subphase. Internal exceptions bubble up to the caller which wraps in try/except.

    Args:
        slug: Pipeline slug being finalized.

    Returns:
        Summary dict with: slug, roles_scanned, critical_created, state_updated.
    """
    root = _project_root()

    # 1. Scan
    entries = scan_for_cargo_roles(slug)
    if not entries:
        logger.debug("role_tracker: no role mentions found for slug=%s", slug)
        return {
            "slug": slug,
            "roles_scanned": 0,
            "critical_created": [],
            "state_updated": False,
        }

    # 2. Merge into tracking state
    update_role_tracking_state(slug, entries, root=root)

    # 3. Auto-create CRITICAL agents that don't exist yet
    # D4 gate: each CRITICAL role asks decision_gateway before auto-creating.
    # Non-blocking: if gateway is unavailable, default to False (do not create)
    # to avoid unreviewed auto-creation noise.
    created: list[str] = []
    for entry in entries:
        if entry["priority"] != "CRITICAL":
            continue
        if entry["agent_exists"]:
            continue
        role_name = entry["normalized_name"]
        area = ROLE_AREA_MAP.get(role_name)
        if not area:
            logger.debug(
                "role_tracker: role '%s' has no area mapping, skipping auto-creation",
                role_name,
            )
            continue

        # D4 — prompt user (or log in non-interactive mode) before auto-creating
        _d4_approved = False
        try:
            from engine.intelligence.pipeline.mce.decision_gateway import (
                decide_d4_create_cargo_agent,
            )

            _d4_approved = decide_d4_create_cargo_agent(
                role=role_name,
                count=entry["mention_count"],
                slug=slug,
            )
            logger.info(
                "role_tracker: D4 decision for role=%s count=%d → %s",
                role_name,
                entry["mention_count"],
                _d4_approved,
            )
        except Exception as _d4_exc:
            logger.warning(
                "role_tracker: D4 decision_gateway unavailable for role=%s, defaulting to False: %s",
                role_name,
                _d4_exc,
            )

        if not _d4_approved:
            logger.info(
                "role_tracker: D4=False — skipping auto-creation for role=%s (slug=%s)",
                role_name,
                slug,
            )
            continue

        result = cmd_create_cargo_agent(
            role_name=role_name,
            area=area,
            sources=entry["sources"],
            root=root,
        )
        if result.get("created"):
            created.append(role_name)
            # Refresh agent_exists in tracking state after creation
            state = _load_cargo_tracking(root)
            if role_name in state.get("roles", {}):
                slug_role = _role_slug(role_name)
                state["roles"][role_name]["agent_exists"] = True
                state["roles"][role_name]["agent_path"] = (
                    f"agents/external/cargo/{area}/{slug_role}/"
                )
                _save_cargo_tracking(root, state)

    logger.info(
        "role_tracker: slug=%s entries=%d critical_created=%s",
        slug,
        len(entries),
        created,
    )
    return {
        "slug": slug,
        "roles_scanned": len(entries),
        "critical_created": created,
        "state_updated": True,
    }
