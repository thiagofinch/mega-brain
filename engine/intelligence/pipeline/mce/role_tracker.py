def cmd_create_cargo_agent(
    role_name: str,
    area: str,
    sources: list[str],
    root: Path | None = None,
) -> dict[str, Any]:
    """Create minimal cargo agent directory structure.

    Creates:
        agents/external/cargo/{area}/{role-slug}/
            AGENT.md
            SOUL.md
            MEMORY.md

    Idempotent: if AGENT.md already exists, returns early with agent_exists=True.

    Args:
        role_name: Normalized role name (e.g., "Head of Ops").
        area: Area folder (e.g., "ops", "sales", "c-level").
        sources: List of slugs that mentioned this role.
        root: Project root (resolved automatically if None).

    Returns:
        Result dict with keys: created (bool), path (str), skipped_reason (str|None).
    """
    if root is None:
        root = _project_root()

    slug_role = _role_slug(role_name)
    agent_dir = root / "agents" / "external" / "cargo" / area / slug_role
    agent_md_path = agent_dir / "AGENT.md"

    if agent_md_path.exists():
        return {
            "created": False,
            "path": str(agent_dir.relative_to(root)),
            "skipped_reason": "AGENT.md already exists",
        }

    agent_dir.mkdir(parents=True, exist_ok=True)
    created_date = datetime.now(UTC).date().isoformat()
    sources_str = ", ".join(sources) if sources else "unknown"
    role_upper = role_name.upper().replace(" ", "_")
    area_title = area.replace("-", " ").title()
    padded = role_name.upper().center(48)
    padded = padded[:48]  # max 48 chars to fit box

    agent_md_path.write_text(
        _AGENT_MD_TEMPLATE.format(
            slug_role=slug_role,
            role_name=role_name,
            area=area,
            area_title=area_title,
            role_upper=role_upper,
            created_date=created_date,
            threshold=THRESHOLD_CRITICAL,
            sources_str=sources_str,
        ),
        encoding="utf-8",
    )

    (agent_dir / "SOUL.md").write_text(
        _SOUL_MD_TEMPLATE.format(
            slug_role=slug_role,
            role_name=role_name,
            role_upper=role_name.upper(),
            role_upper_padded=padded,
            created_date=created_date,
        ),
        encoding="utf-8",
    )

    (agent_dir / "MEMORY.md").write_text(
        _MEMORY_MD_TEMPLATE.format(
            role_name=role_name,
            area=area,
            slug_role=slug_role,
            created_date=created_date,
            threshold=THRESHOLD_CRITICAL,
        ),
        encoding="utf-8",
    )

    rel_path = str(agent_dir.relative_to(root))
    logger.info(
        "role_tracker: auto-created cargo agent %s at %s (sources: %s)",
        role_name,
        rel_path,
        sources_str,
    )
    print(
        f"[role_tracker] AUTO-CREATED cargo agent '{role_name}' -> {rel_path}",
        flush=True,
    )
    return {
        "created": True,
        "path": rel_path,
        "skipped_reason": None,
    }


