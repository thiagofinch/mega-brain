"""Role Tracker — Cargo Agent Auto-Creation.

Story MCE-11.8: Detects cargo role mentions in per-slug INSIGHTS-STATE.json,
maintains a ROLE-TRACKING-CARGO.json registry, and auto-creates minimal
cargo agent scaffolds when a role reaches the CRITICAL threshold (>= 10 mentions).

Design decisions:
- No LLM calls: keyword-based detection only (cost efficiency, precision over recall).
- Non-blocking from cmd_finalize: exceptions are caught and logged as warnings.
- Coexists with existing ROLE-TRACKING.json (keyed by person, different schema).
  This module writes ROLE-TRACKING-CARGO.json only.
- Auto-creation only for roles defined in ROLE_AREA_MAP. Unknown roles are tracked
  but never auto-created to prevent noise.
- Idempotent: if agent directory already exists, skips creation silently.
- Semantic filter (Story MCE-semantic-filter): PASSIVE mentions (citations of third
  parties) are excluded from counts. Only ACTIVE mentions (attributed perspective,
  imperatives, possessive 1st person) advance the counter. Threshold remains 10.

AUTO-GENERATED by role_tracker.py [Story MCE-11.8]
"""

from __future__ import annotations

import json
import logging
import re
from datetime import UTC, datetime
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

THRESHOLD_CRITICAL = 10
THRESHOLD_IMPORTANT = 5
# 1-4 = TRACK

# ---------------------------------------------------------------------------
# Semantic filter: PASSIVE vs ACTIVE mention classification
# ---------------------------------------------------------------------------

# Patterns that strongly signal a PASSIVE mention (citation of a third party,
# not an attributed perspective). Matched against a ±80-char window around
# the role keyword match.
PASSIVE_MARKERS_REGEX: list[str] = [
    r"\bde\s+(uma|um)\s+(empresa|cliente|consultoria)\b",  # "de uma empresa", "de um cliente"
    r"\bconheci\s+(um|uma)\b",                              # "conheci um Closer"
    r"\bconsultei\s+(um|uma)\b",                            # "consultei um CFO"
    r"\bvi\s+(um|uma)\b",                                   # "vi um BDR fazer"
    r"\b(do|da|dos|das)\s+meu(s)?\s+(cliente|amigo|conhecido)",  # "do meu cliente"
    r"\bcliente\s+(meu|nosso|dele|dela)\b",                 # "cliente meu CFO"
    r"\b(esse|essa|aquele|aquela)\s+(cara|moço|mocо|pessoa)\b",  # "esse cara CFO"
]

# Patterns that strongly signal an ACTIVE mention (attributed perspective,
# imperative, possessive 1st person tied to the role).
ACTIVE_MARKERS_REGEX: list[str] = [
    r"\bcomo\s+(um|uma|o|a)\s+",                            # "como um CFO"
    r"\bse\s+você\s+(é|for|fosse)\s+",                      # "se você é CFO"
    r"\bvocê\s+precisa\s+",                                 # "você precisa"
    r"\b(faça|pense|aja|seja)\s+",                          # imperatives
    r"\b(meu|minha|nosso|nossa)\s+(cfo|cro|coo|closer|bdr|setter|sds|lns)",  # possessive 1st person (lowercased)
    r"\bo\s+(papel|trabalho|função|funcao|job)\s+d[oa]\s+",  # "o papel do CFO"
    r"\bdeve(m)?\s+(fazer|pensar|usar|aplicar)\b",           # "o Closer deve fazer X"
]

# Window size (chars) around the keyword match used for context evaluation
_CLASSIFY_WINDOW = 80


def _classify_mention(text: str, match_start: int, match_end: int) -> str:
    """Classify a role keyword match as ACTIVE, PASSIVE, or AMBIGUOUS.

    Extracts a ±_CLASSIFY_WINDOW-char window around the match and tests it
    against ACTIVE and PASSIVE marker patterns.

    Returns:
        "ACTIVE"    — at least one ACTIVE_MARKER matched (wins over PASSIVE).
        "PASSIVE"   — at least one PASSIVE_MARKER matched AND no ACTIVE marker.
        "AMBIGUOUS" — neither ACTIVE nor PASSIVE matched; treated as ACTIVE
                      (fail-open: avoid false negatives).

    Examples:
        >>> _classify_mention("o cfo de uma empresa que consultei fez X", 2, 5)
        'PASSIVE'
        >>> _classify_mention("pense como um cfo na hora de avaliar", 14, 17)
        'ACTIVE'
        >>> _classify_mention("o cfo definiu a estrategia do trimestre", 2, 5)
        'AMBIGUOUS'
    """
    window_start = max(0, match_start - _CLASSIFY_WINDOW)
    window_end = min(len(text), match_end + _CLASSIFY_WINDOW)
    window = text[window_start:window_end]

    # Check ACTIVE first — it wins even if PASSIVE also matches
    for pattern in ACTIVE_MARKERS_REGEX:
        if re.search(pattern, window):
            return "ACTIVE"

    for pattern in PASSIVE_MARKERS_REGEX:
        if re.search(pattern, window):
            return "PASSIVE"

    return "AMBIGUOUS"  # default: treat as ACTIVE (fail-open)


# Keyword groups -> normalized role name.
