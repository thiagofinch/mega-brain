def scan_for_cargo_roles(slug: str) -> list[dict[str, Any]]:
    """Scan per-slug INSIGHTS-STATE.json and count cargo role keyword mentions.

    Args:
        slug: Pipeline slug (e.g., "LO-0001").

    Returns:
        List of RoleEntry dicts with keys:
            normalized_name, mention_count, sources, priority, agent_exists, agent_path
    """
    root = _project_root()
    artifacts_dir = root / ".data" / "artifacts"

    # Canonical path (matches orchestrate._insights_state_path):
    #   .data/artifacts/insights/{slug}/INSIGHTS-STATE.json
    insights_path = artifacts_dir / "insights" / slug / "INSIGHTS-STATE.json"
    if not insights_path.exists():
        # Secondary: .data/artifacts/mce/{slug}/INSIGHTS-STATE.json (legacy MCE layout)
        insights_path = artifacts_dir / "mce" / slug / "INSIGHTS-STATE.json"
    if not insights_path.exists():
        # Tertiary: flat layout .data/artifacts/{slug}/INSIGHTS-STATE.json (pre-migration)
        insights_path = artifacts_dir / slug / "INSIGHTS-STATE.json"
    if not insights_path.exists():
        # Final fallback: .data/artifacts/INSIGHTS-STATE-{slug}.json
        insights_path = artifacts_dir / f"INSIGHTS-STATE-{slug}.json"
    if not insights_path.exists():
        logger.debug("role_tracker: INSIGHTS-STATE not found for slug=%s, skipping scan", slug)
        return []

    try:
        raw = json.loads(insights_path.read_text(encoding="utf-8"))
    except Exception as exc:
        logger.warning("role_tracker: failed to read INSIGHTS-STATE for %s: %s", slug, exc)
        return []

    # Multi-schema detection (MCE-11.8):
    # Schema A: legacy lista direta
    if isinstance(raw, list):
        insights = raw
    # Schema B: top-level {"insights": [...]}
    elif isinstance(raw, dict) and "insights" in raw and isinstance(raw["insights"], list):
        insights = raw["insights"]
    # Schema C: aninhado {"persons": {"Name": {"insights": [...]}}}
    elif isinstance(raw, dict) and "persons" in raw and isinstance(raw["persons"], dict):
        insights = []
        for person_data in raw["persons"].values():
            if isinstance(person_data, dict):
                pi = person_data.get("insights", [])
                if isinstance(pi, list):
                    insights.extend(pi)
    # Schema D: wrapper {"insights_state": {"persons": {"Name": {"insights": [...]}}}}
    elif isinstance(raw, dict) and "insights_state" in raw and isinstance(raw["insights_state"], dict):
        inner = raw["insights_state"]
        insights = []
        persons_map = inner.get("persons", {})
        if isinstance(persons_map, dict):
            for person_data in persons_map.values():
                if isinstance(person_data, dict):
                    pi = person_data.get("insights", [])
                    if isinstance(pi, list):
                        insights.extend(pi)
    else:
        insights = []
        logger.warning("role_tracker: unknown INSIGHTS-STATE schema for slug=%s", slug)

    # Count per-role across all insight text fields.
    # active_counts: mentions that passed the semantic filter (ACTIVE or AMBIGUOUS).
    # passive_counts: mentions rejected as PASSIVE citations of third parties.
    active_counts: dict[str, int] = {role: 0 for role in CARGO_ROLE_KEYWORDS}
    passive_counts: dict[str, int] = {role: 0 for role in CARGO_ROLE_KEYWORDS}

    def _search_text(text: str) -> None:
        if not text:
            return
        lowered = text.lower()
        for role_name, patterns in CARGO_ROLE_KEYWORDS.items():
            for pattern in patterns:
                # Patterns starting with \b are regex, rest are plain substring
                if pattern.startswith(r"\b") or pattern.startswith("(?"):
                    match = re.search(pattern, lowered)
                    if match:
                        classification = _classify_mention(
                            lowered, match.start(), match.end()
                        )
                        if classification == "PASSIVE":
                            passive_counts[role_name] += 1
                        else:
                            # ACTIVE or AMBIGUOUS both advance the active counter
                            active_counts[role_name] += 1
                        break  # count once per insight-field per role
                else:
                    idx = lowered.find(pattern)
                    if idx != -1:
                        classification = _classify_mention(
                            lowered, idx, idx + len(pattern)
                        )
                        if classification == "PASSIVE":
                            passive_counts[role_name] += 1
                        else:
                            active_counts[role_name] += 1
                        break

    for insight in insights:
        if not isinstance(insight, dict):
            continue
        _search_text(insight.get("insight", ""))
        _search_text(insight.get("quote", ""))
        tags = insight.get("tags", insight.get("tag", ""))
        if isinstance(tags, list):
            _search_text(" ".join(tags))
        else:
            _search_text(str(tags) if tags else "")

    # Telemetry log: one line per role that had any mention (active or passive)
    for role_name in CARGO_ROLE_KEYWORDS:
        a = active_counts[role_name]
        p = passive_counts[role_name]
        if a + p > 0:
            logger.info(
                "role_tracker: slug=%s role=%-20s active=%d passive_filtered=%d",
                slug,
                role_name,
                a,
                p,
            )

    # Build result entries (only roles with at least 1 ACTIVE mention).
    # mention_count = active_count for backward compatibility with threshold logic.
    cargo_agents_root = root / "agents" / "external" / "cargo"
    entries = []
    for role_name in CARGO_ROLE_KEYWORDS:
        count = active_counts[role_name]
        if count == 0:
            continue
        area = ROLE_AREA_MAP.get(role_name)
        slug_role = _role_slug(role_name)
        agent_path_rel: str | None = None
        agent_exists = False
        if area:
            candidate = cargo_agents_root / area / slug_role / "AGENT.md"
            if candidate.exists():
                agent_exists = True
                agent_path_rel = f"agents/external/cargo/{area}/{slug_role}/"
        entries.append(
            {
                "normalized_name": role_name,
                "mention_count": count,           # ACTIVE only — threshold uses this
                "passive_count_filtered": passive_counts[role_name],
                "sources": [slug],
                "priority": _compute_priority(count),
                "agent_exists": agent_exists,
                "agent_path": agent_path_rel,
            }
        )

    return entries


