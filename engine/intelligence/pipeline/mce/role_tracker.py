def _load_cargo_tracking(root: Path) -> dict[str, Any]:
    tracking_path = root / ".data" / "artifacts" / "ROLE-TRACKING-CARGO.json"
    if tracking_path.exists():
        try:
            return json.loads(tracking_path.read_text(encoding="utf-8"))
        except Exception as exc:
            logger.warning("role_tracker: could not parse ROLE-TRACKING-CARGO.json: %s", exc)
    return {"last_updated": "", "roles": {}}


def _save_cargo_tracking(root: Path, state: dict[str, Any]) -> None:
    tracking_path = root / ".data" / "artifacts" / "ROLE-TRACKING-CARGO.json"
    tracking_path.parent.mkdir(parents=True, exist_ok=True)
    tracking_path.write_text(
        json.dumps(state, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )


def update_role_tracking_state(
    slug: str,
    new_entries: list[dict[str, Any]],
    root: Path | None = None,
) -> dict[str, Any]:
    """Merge new role entries into ROLE-TRACKING-CARGO.json.

    For each role entry from the current scan, merges mention counts and
    updates source list. Recalculates priority after merge.

    Args:
        slug: Source slug being finalized.
        new_entries: Output of scan_for_cargo_roles(slug).
        root: Project root (resolved automatically if None).

    Returns:
        Updated tracking state dict.
    """
    if root is None:
        root = _project_root()
    state = _load_cargo_tracking(root)
    roles: dict[str, Any] = state.get("roles", {})

    cargo_agents_root = root / "agents" / "external" / "cargo"

    for entry in new_entries:
        role_name = entry["normalized_name"]
        area = ROLE_AREA_MAP.get(role_name)
        slug_role = _role_slug(role_name)

        # Refresh agent_exists on every update (agent may have been created since last run)
        agent_exists = False
        agent_path_rel: str | None = None
        if area:
            candidate = cargo_agents_root / area / slug_role / "AGENT.md"
            if candidate.exists():
                agent_exists = True
                agent_path_rel = f"agents/external/cargo/{area}/{slug_role}/"

        if role_name not in roles:
            roles[role_name] = {
                "normalized_name": role_name,
                "mention_count": entry["mention_count"],
                "sources": list(entry["sources"]),
                "priority": entry["priority"],
                "agent_exists": agent_exists,
                "agent_path": agent_path_rel,
                "last_updated": datetime.now(UTC).isoformat(),
            }
        else:
            existing = roles[role_name]
            existing["mention_count"] = existing.get("mention_count", 0) + entry["mention_count"]
            sources_set = set(existing.get("sources", []))
            sources_set.update(entry["sources"])
            existing["sources"] = sorted(sources_set)
            existing["priority"] = _compute_priority(existing["mention_count"])
            existing["agent_exists"] = agent_exists
            existing["agent_path"] = agent_path_rel
            existing["last_updated"] = datetime.now(UTC).isoformat()

    state["roles"] = roles
    state["last_updated"] = datetime.now(UTC).isoformat()
    _save_cargo_tracking(root, state)
    return state


