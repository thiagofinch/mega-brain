def _project_root() -> Path:
    """Resolve project root via git toplevel or fallback to 4 levels up from this file."""
    import subprocess

    try:
        result = subprocess.run(
            ["git", "rev-parse", "--show-toplevel"],
            capture_output=True,
            text=True,
            check=True,
        )
        return Path(result.stdout.strip())
    except Exception:
        # Fallback: engine/intelligence/pipeline/mce/ -> 4 levels up
        return Path(__file__).resolve().parents[4]


def _role_slug(role_name: str) -> str:
    """Convert 'Head of Ops' -> 'head-of-ops'."""
    return re.sub(r"[^a-z0-9]+", "-", role_name.lower()).strip("-")


def _compute_priority(count: int) -> str:
    if count >= THRESHOLD_CRITICAL:
        return "CRITICAL"
    if count >= THRESHOLD_IMPORTANT:
        return "IMPORTANT"
    return "TRACK"


