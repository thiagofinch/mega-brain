def get_agent_filenames(agent_dir: Path) -> dict[str, str]:
    """Return canonical filenames for agent files in the given directory.

    MCE-13.6: external/business/personal agents use lowercase filenames.
    Cargo agents (``agents/external/cargo/**``) retain UPPERCASE for
    backward compat (MCE-12.1 carve-out).

    Args:
        agent_dir: Absolute or repo-relative Path to the agent directory.

    Returns:
        Dict with keys ``agent``, ``soul``, ``memory``, ``dna_config``
        mapping to the correct filename for the bucket.
    """
    parts = Path(agent_dir).parts
    is_cargo = "cargo" in parts
    if is_cargo:
        return {
            "agent": "AGENT.md",
            "soul": "SOUL.md",
            "memory": "MEMORY.md",
            "dna_config": "DNA-CONFIG.yaml",
        }
    return {
        "agent": "agent.md",
        "soul": "soul.md",
        "memory": "memory.md",
        "dna_config": "dna-config.yaml",
    }


def _slug_to_person_name(slug: str) -> str:
    """Convert a kebab-case slug to a display name."""
    return " ".join(w.capitalize() for w in slug.split("-"))


def _dna_dir_for_bucket(slug: str, bucket: str):
    """Delegate to the single SOT (STORY-MCE-BUCKET-AWARE-WRITES).

    Previously duplicated orchestrate._dna_dir_for_slug with the deprecated
    convention A (business -> ``people/{slug}``). Now both delegate to
    ``PersonArtifactPaths`` (convention B, ``dna/persons/{slug}``) so the
    skeleton reader never diverges from the writer.
    """
    from engine.intelligence.pipeline.mce.person_paths import PersonArtifactPaths

    return PersonArtifactPaths(slug, bucket, root=ROOT).dna


def _read_dna_summary(slug: str, bucket: str) -> dict[str, Any]:
    """Read DNA-CONFIG.yaml composition for a slug (best-effort)."""
    dna_dir = _dna_dir_for_bucket(slug, bucket)
    dna_path = dna_dir / "DNA-CONFIG.yaml"
    if not dna_path.exists():
        return {}
    try:
        with open(dna_path, encoding="utf-8") as fh:
            data = yaml.safe_load(fh) or {}
    except Exception as exc:  # pragma: no cover - non-fatal
        logger.debug("Failed to read DNA-CONFIG for %s: %s", slug, exc)
        return {}
    if not isinstance(data, dict):
        return {}
    return data


def create_agent_skeleton(
    slug: str,
    bucket: str,
    *,
    sources_count: int = 0,
    dry_run: bool = False,
) -> dict[str, Any]:
    """Create a minimal agent skeleton for a slug in the given bucket.

    Args:
        slug: Person slug (kebab-case).
        bucket: One of ``"external" | "business"``. ``"personal"`` is rejected
            (personal agents are hand-authored, never auto-generated).
        sources_count: Optional count of source files processed (for AGENT.md
            metadata).
        dry_run: When True, return what WOULD be created without writing.

    Returns:
        Dict with keys:
            - ``created`` (bool): True if a new skeleton was written.
            - ``agent_dir`` (str): Absolute path to the agent directory.
            - ``files`` (list[str]): Files written (or to be written).
            - ``reason`` (str, optional): Reason when ``created=False``.
            - ``error`` (str, optional): Error message on failure.

    Never raises -- caller (cmd_finalize) must remain non-blocking.
    """
    if bucket not in ("external", "business"):
        return {
            "created": False,
            "reason": f"bucket={bucket!r} not eligible for auto-skeleton",
            "agent_dir": "",
            "files": [],
        }

    if not slug or "/" in slug or ".." in slug:
        return {
            "created": False,
            "reason": f"invalid slug={slug!r}",
            "agent_dir": "",
            "files": [],
        }

    agent_dir = ROOT / "agents" / bucket / slug

    if agent_dir.exists():
        return {
            "created": False,
            "reason": "agent directory already exists",
            "agent_dir": str(agent_dir),
            "files": [],
        }

    # Gather DNA evidence -- the entire purpose of auto-skeleton is to capture
    # a slug that has DNA but no agent.  If DNA is missing, skip (the slug is
    # not yet ripe for an agent shell).
    dna_data = _read_dna_summary(slug, bucket)
    if not dna_data:
        _expected_dir = _dna_dir_for_bucket(slug, bucket).relative_to(ROOT)
        return {
            "created": False,
            "reason": f"no DNA-CONFIG.yaml at {_expected_dir}/",
            "agent_dir": str(agent_dir),
            "files": [],
        }

    composition = dna_data.get("composition", {}) if isinstance(dna_data, dict) else {}
    dna_total = composition.get("total", 0) if isinstance(composition, dict) else 0
    dna_layers = (
        len([k for k in composition if k != "total"]) if isinstance(composition, dict) else 0
    )

    person_name = _slug_to_person_name(slug)
    date = datetime.now(UTC).strftime("%Y-%m-%d")

    # MCE-13.6: resolve bucket-aware filenames (lowercase for non-cargo)
    fnames = get_agent_filenames(agent_dir)

    file_plan = {
        fnames["agent"]: _AGENT_MD_TEMPLATE.format(
            slug=slug,
            person_name=person_name,
            bucket=bucket,
            date=date,
            sources_count=sources_count,
            dna_layers=dna_layers,
            dna_total=dna_total,
        ),
        fnames["soul"]: _SOUL_MD_TEMPLATE.format(
            slug=slug,
            person_name=person_name,
            bucket=bucket,
            date=date,
        ),
        fnames["memory"]: _MEMORY_MD_TEMPLATE.format(
            slug=slug,
            person_name=person_name,
            date=date,
        ),
        fnames["dna_config"]: yaml.safe_dump(
            {
                "id": slug,
                "person": person_name,
                "bucket": bucket,
                "status": "skeleton",
                "auto_generated": True,
                "generated_at": date,
                "dna_sources": {
                    "primario": [
                        {
                            "pessoa": slug,
                            "weight": 1.0,
                            "artifact_dir": f"knowledge/{bucket}/dna/persons/{slug}/",
                        }
                    ]
                },
                "composition": composition,
            },
            default_flow_style=False,
            sort_keys=False,
            allow_unicode=True,
        ),
    }

    if dry_run:
        return {
            "created": False,
            "reason": "dry_run",
            "agent_dir": str(agent_dir),
            "files": list(file_plan.keys()),
        }

    try:
        agent_dir.mkdir(parents=True, exist_ok=False)
    except FileExistsError:
        # Race: another process created it between our exists() check and now.
        return {
            "created": False,
            "reason": "race -- agent directory appeared mid-creation",
            "agent_dir": str(agent_dir),
            "files": [],
        }
    except Exception as exc:
        return {
            "created": False,
            "error": f"mkdir failed: {exc}",
            "agent_dir": str(agent_dir),
            "files": [],
        }

    written: list[str] = []
    for fname, content in file_plan.items():
        try:
            (agent_dir / fname).write_text(content, encoding="utf-8")
            written.append(fname)
        except Exception as exc:  # pragma: no cover - non-fatal
            logger.warning("Failed to write %s for %s: %s", fname, slug, exc)

    logger.info(
        "Auto-created agent skeleton at %s (%d files, bucket=%s, dna_total=%d)",
        agent_dir,
        len(written),
        bucket,
        dna_total,
    )

    return {
        "created": True,
        "agent_dir": str(agent_dir),
        "files": written,
        "bucket": bucket,
        "dna_total": dna_total,
    }


def ensure_agent_skeleton_for_slug(
    slug: str,
    bucket: str,
    *,
    sources_count: int = 0,
) -> dict[str, Any]:
    """Idempotent helper: ensure an agent skeleton exists for the slug.

    Wrapper around ``create_agent_skeleton`` that swallows ALL exceptions and
    returns a structured result.  Designed to be called from ``cmd_finalize``
    where pipeline integrity (Art. XII) requires non-fatal observability.

    Args:
        slug: Person slug (kebab-case).
        bucket: ``"external" | "business"``.
        sources_count: Optional count of source files processed.

    Returns:
        Same shape as ``create_agent_skeleton``.  On unexpected exception
        returns ``{"created": False, "error": ..., "agent_dir": "", "files": []}``.
    """
    try:
        return create_agent_skeleton(slug, bucket, sources_count=sources_count)
    except Exception as exc:  # pragma: no cover - defensive
        logger.warning("ensure_agent_skeleton_for_slug failed for %s: %s", slug, exc)
        return {
            "created": False,
            "error": str(exc),
            "agent_dir": "",
            "files": [],
        }
