"""agent_skeleton.py -- Auto-create minimal agent skeleton for new slugs
=======================================================================

G18 (2026-05-13): when the MCE pipeline finalizes a slug whose person agent
does NOT exist yet, downstream cascading + memory enrichment silently emit
``0 appended`` because there is no ``agents/{bucket}/{slug}/`` to target.

This module creates the minimal four-file skeleton expected by the agent
boot sequence (AGENT.md → SOUL.md → MEMORY.md → DNA-CONFIG.yaml) so that
``memory_enricher.enrich_from_insights_state`` finds a valid target on the
very first run.

Skeletons are EXPLICITLY marked as auto-generated ("status: skeleton"). They
are NOT mind-clones until populated by subsequent cascade enrichment and
human review.

Contract:
    - Idempotent: never overwrites an existing agent directory.
    - Non-fatal: any exception is logged and ``created=False`` is returned.
    - Confined to ``agents/external/`` and ``agents/business/`` (no personal).
    - Files are minimal-by-design: SOUL/AGENT are templates, MEMORY is the
      same stub the enricher would create, DNA-CONFIG references the canonical
      DNA artifact path under ``knowledge/{bucket}/dna/persons/{slug}/``.

Version: 1.0.0
"""

